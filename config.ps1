#Requires -Version 5.1

<#
.SYNOPSIS
    ASCII 视频播放器配置文件。

.DESCRIPTION
    所有可调参数集中管理。修改此文件中的变量即可
    自定义播放行为，无需修改主脚本。

.NOTES
    在 Play-AsciiVideo.ps1 中通过 dot-source 加载：
        . "$PSScriptRoot\config.ps1"
    命令行参数会覆盖此文件中的默认值。
#>

# ============================================================
# 字符映射
# ============================================================

# 字符梯度：从左到右表示从暗到亮（低灰度→高灰度）
# 灰度值 0-255 将线性映射到这个字符串的每个字符
$Global:CharGradient = " .:-=+*#%@"

# ============================================================
# 播放参数
# ============================================================

# 目标帧率（0 = 自动检测视频原始帧率）
$Global:TargetFps = 0

# 最大帧宽度（0 = 自动使用控制台窗口宽度）
$Global:MaxFrameWidth = 0

# 最大帧高度（0 = 自动使用控制台窗口高度 - 2，留边距）
$Global:MaxFrameHeight = 0

# 最大处理帧数（防止长视频内存溢出）
# 3000 帧 ≈ 100 秒 @ 30fps
$Global:MaxFrameCount = 3000

# ============================================================
# 路径配置
# ============================================================

# 临时帧图片目录
$Global:TempDir = "$PSScriptRoot\temp"

# 是否保留临时帧文件（调试用）
$Global:KeepFrames = $false

# ============================================================
# 控制台配置
# ============================================================

# 播放时是否隐藏控制台光标
$Global:HideCursor = $true

# 是否清屏后再播放
$Global:ClearScreenBeforePlay = $true

# ============================================================
# 缓存配置
# ============================================================

# 是否启用帧缓存（启用后二次播放同视频无需拆帧和转换）
$Global:CacheEnabled = $true

# 缓存文件存储目录
$Global:CacheDir = "$PSScriptRoot\cache"

# 缓存过期天数（超过自动失效，0 表示永不过期）
$Global:CacheExpireDays = 30

# ============================================================
# 实验性功能
# ============================================================

# 是否启用彩色输出（实验性，可能影响性能）
$Global:ColorMode = $false
